//for
for (var i = 0; i < 10; i++) {
  console.log(i); //Output 0, 1, 2, 3... 9
}

for (var j = 10; j > 0; j--) {
  console.log(j); //Output 10, 9, 8... 1
}


console.log('\n\n');

//while
var k = 0;
while (k < 10) {
  console.log(k);
  k++;
}


console.log('\n\n');
//do while
var z = 10;
do {
  console.log(z);
  z--;
} while(z > 0);
